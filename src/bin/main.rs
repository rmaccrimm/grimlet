use capstone::Capstone;
use capstone::arch::BuildsCapstone as _;
use capstone::arch::arm::ArchMode;
use clap::Parser;

#[derive(Parser)]
struct Args {
    n: Vec<i32>,
}

fn main() -> anyhow::Result<()> {
    let buf = [
        0x01, 0x00, 0x81, 0xe2, 0x02, 0x00, 0xa0, 0xe1, 0x04, 0x20, 0x9a, 0xe5, 0x01, 0x00, 0xbd,
        0xe8, 0x08, 0x60, 0x47, 0xe0, 0x04, 0x00, 0x00, 0xeb, 0x02, 0x00, 0x81, 0xe0, 0x00, 0x02,
        0x2d, 0xe9, 0x01, 0x00, 0x00, 0xeb,
    ];
    let cs = Capstone::new()
        .arm()
        .mode(ArchMode::Arm)
        .detail(true)
        .build()?;
    let ins = cs.disasm_all(&buf, 0)?;
    for instr in ins.iter() {
        println!("{instr}");
    }
    Ok(())
}
